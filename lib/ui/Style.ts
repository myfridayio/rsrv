export const shadow = (d: Direction, c: Color=Colors.black, { offset: off, opacity, radius }: { offset?: number, opacity?: number, radius?: number }={}) => ({ shadowColor: c.hex, shadowOffset: offset(d, off||4), shadowOpacity: opacity||.3, shadowRadius: radius||4 })


const offset = (d: Direction, offset: number) => {
  switch (d) {
    case 'north':
      return { width: 0, height: -offset }
    case 'south':
      return { width: 0, height: offset }
    case 'east':
      return { width: offset, height: 0 }
    case 'west':
      return { width: -offset, height: 0 }
    case 'northwest':
      return { width: -offset, height: -offset }
    case 'northeast':
      return { width: offset, height: -offset }
    case 'southwest':
      return { width: -offset, height: offset }
    case 'southeast':
      return { width: offset, height: offset }
  }
}

export type Direction = 'north'|'south'|'east'|'west'|'northwest'|'northeast'|'southwest'|'southeast'

